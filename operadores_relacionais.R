# operadores relacionais 

x = 5
y = 2

print(x > y);     # maior
print(x < y);     # menor
print(x >= y);    # maior igual
print(x <= y);    # menor igual
print(x == y);    # igual
print(x != y);    # diferente
print(5 == '5')   # R não compara o tipo,mas o conteudo em si
print(5L == '5L') # Só nesse caso que não da TRUE.
print(5L == '5')  # Nesse da True
